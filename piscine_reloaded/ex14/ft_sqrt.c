int		ft_sqrt(int nb)
{
	int		i;

	i = 2;
	if (nb > 1)
	{
		while ((i * i) <= (nb))
		{
			if ((i * i) == nb)
				return (i);
			i++;
		}
	}
	if (nb == 1 || nb == 0)
		return (nb);
	return (0);
}
