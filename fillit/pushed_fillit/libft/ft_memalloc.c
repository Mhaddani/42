#include "libft.h"

void	*ft_memalloc(size_t size)
{
	char	*r;
	size_t	i;

	if (!(r = (char *)malloc(size)))
		return (NULL);
	i = 0;
	while (i < size)
		r[i++] = '\0';
	return (r);
}
